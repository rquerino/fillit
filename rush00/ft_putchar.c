#include <unistd.h>

void	ft_putchar(int e, int x)
{
	int i;

	i = 1;
	while (i <= x)
	{
		if ((i == 1) && (e == 1))
		{
			write(1, "A", 1);
		}
		else if ((i == x) && (e == 1))
		{
			write(1, "C", 1);
		}
		else if (((i != 1) && (i != x)) && (e == 0))
		{
			write(1, " ", 1);
		}
		else
		{
			write(1, "B", 1);
		}
		i++;
	}
}
